
package ec.edu.espoch.claseuno.clases;


public class Horario {
    private int numeroHorasTrabajo;
    private String diaLaborable; 
    
    public Horario(){
        
    }
    

    public int getNumeroHorasTrabajo() {
        return numeroHorasTrabajo;
    }

    public void setNumeroHorasTrabajo(int numeroHorasTrabajo) {
        this.numeroHorasTrabajo = numeroHorasTrabajo;
    }

    public String getDiaLaborable() {
        return diaLaborable;
    }

    public void setDiaLaborable(String diaLaborable) {
        this.diaLaborable = diaLaborable;
    }
    

    
}
